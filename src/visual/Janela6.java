private void preencherTabela() {
        
    try {

            //Buscando objeto ProdutoServicos     
            ConsultaServicos ps = ServicosFactory.getConsultaServicos();

            /* Criando um ArrayList<ProdutoVO> vazio
             para receber o ArrayList com os dados */
            ArrayList<Consulta> prod = new ArrayList<>();

            //Recebendo o ArrayList cheio em produtos
            prod = ps.buscarConsultas();

            for (int i = 0; i < prod.size(); i++) {
                dtm.addRow(new String[]{
                    String.valueOf(prod.get(i).getCodigo()),
                    String.valueOf(prod.get(i).getData()),
                    String.valueOf(prod.get(i).getHora()),
                    String.valueOf(prod.get(i).getCrm()),
                    String.valueOf(prod.get(i).getMatricula())
                    
                });
            }//fecha for

            /* Adicionando o modelo de tabela 
             com os dados na tabela produto */
            jtConsulta.setModel(dtm);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro! " + e.getMessage());
        }//fecha catch
    }//fecha p
private void limparTabela() {
        /* Para limpar a tabela temos que setar o nÃºmero de
           linhas para zero no default table model */        
        dtm.setNumRows(0);
    }//fecha limparTabela
